public class Book {
    
    public String title;
    public String author;
    public int yearPublished;
    public int currentPage;

    public void Book(String title, String author, int yearPublished, int currentPage) {
        this.title = title;
        this.author = author;
        this.yearPublished = yearPublished;
        this.currentPage = currentPage; 
    }
}